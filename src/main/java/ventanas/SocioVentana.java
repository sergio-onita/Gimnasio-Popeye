    private void solicitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solicitaActionPerformed
        Socio s1 = new Socio();
        ArrayList<Socio> socios = Util.getArraySocios();
        for (int i = 0; i < socios.size(); i++) {
            if (socios.get(i).getUsuario().equals(this.usuario)) {
                s1 = socios.get(i);
                if (s1.getEntrenador() == null) {
                    ArrayList<Empleado> empleados = Util.getArrayEmpleado();
                    ArrayList<Empleado> entrenadores = new ArrayList<Empleado>();
                    for (int j = 0; j < empleados.size(); j++) {
                        if (empleados.get(j).getTipoEmpleado() == "Entrenador") {
                            entrenadores.add(empleados.get(j));
                        }
                    }
                    Random random = new Random();
                    int value = random.nextInt(entrenadores.size() + 1) + 1;
                    s1.setEntrenador(entrenadores.get(value));
                    JOptionPane.showMessageDialog(null, "Tu nuevo entrenador es: " + s1.getEntrenador().getNombre());
                } else {
                    JOptionPane.showMessageDialog(null, "Tu entrenador es: " + s1.getEntrenador().getNombre());
                }
            }
        }
    }//GEN-LAST:event_solicitaActionPerformed

